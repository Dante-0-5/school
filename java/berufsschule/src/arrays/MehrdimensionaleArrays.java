package arrays;

public class MehrdimensionaleArrays {
	public static void main(String[] args) {
		int[] feld = new int[4]; 
		//eindimensionales int-array
		
		int[][] feld2 = new int[4][3]; 
		//zweidimensionales int-array mit 4 zeilen und 3 spalten
		
		int[][][] feld3 = new int[4][3][2]; 
		//dreidimensionales int-array mit 4 zeilen, 3 spalten und tiefe 2
	}

}
