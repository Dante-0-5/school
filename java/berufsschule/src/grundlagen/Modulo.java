package grundlagen;

public class Modulo {
	public static void main (String [] args) {
		//Modulo beschreibt den Rest einer Divisionsrechnung
		//In Java ist der Modulo-Operator "%"
		
		System.out.println(4%2); //Output: 0
		System.out.println(5%2); //Output: 1
		System.out.println(6%2); //Output: 0
		
		System.out.println(3%3); //Output: 0
		System.out.println(4%3); //Output: 1
		System.out.println(5%3); //Output: 2
		System.out.println(6%3); //Output: 0
		
		System.out.println(1435%12);
		
		//Kann z.B. genutzt werden, um Zahlenreihen zu generieren, oder um eine Prüfung der Teilbarkeit
		//oder der Geradigkeit durchzuführen
		
	}

}
